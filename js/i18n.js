    function onLangChange(fn) {
        if (typeof fn === "function") langChangeListeners.push(fn);
    }

    function notifyLangChange() {
        const lang = getLang();
        langChangeListeners.forEach(fn => {
            try { fn(lang); } catch (_) { /* ignore */ }
        });
    }

    function mergeCatalog(catalog) {
        if (!catalog) return;
        if (catalog.zh) Object.assign(STR.zh, catalog.zh);
        if (catalog.en) Object.assign(STR.en, catalog.en);
        if (catalog.serverErr) Object.assign(SERVER_ERR_ZH_EN, catalog.serverErr);
    }

    function tReplace(key, vars) {
        let s = t(key);
        if (vars) {
            Object.entries(vars).forEach(([k, v]) => {
                s = s.split(`{${k}}`).join(String(v ?? ""));
            });
        }
        return s;
    }

    function trError(msg) {
        if (!msg || getLang() !== "en") return msg || "";
        return SERVER_ERR_ZH_EN[msg] || msg;
    }

    function getLang() {
        const lang = sessionStorage.getItem(SK.uiLang) || "zh";
        return lang === "en" ? "en" : "zh";
    }

    function setLang(lang, force) {
        if (!force && sessionStorage.getItem(SK.uiLangLocked) === "1") return false;
        sessionStorage.setItem(SK.uiLang, lang === "en" ? "en" : "zh");
        syncLangBarButtons();
        notifyLangChange();
        return true;
    }

    function shouldHideLangBar(bar) {
        if (!bar || !isLangLocked()) return false;
        return !bar.classList.contains("lang-bar--inline");
    }

    function applyLangBarVisibility() {
        const bar = document.getElementById("csLangBar");
        if (!bar) return;
        bar.style.display = shouldHideLangBar(bar) ? "none" : "";
        syncLangBarButtons();
    }

    function lockLang() {
        /* Language stays editable throughout the app; clear any legacy lock. */
        sessionStorage.removeItem(SK.uiLangLocked);
        applyLangBarVisibility();
    }

    function unlockLang() {
        sessionStorage.removeItem(SK.uiLangLocked);
        applyLangBarVisibility();
    }

    function isLangLocked() {
        return sessionStorage.getItem(SK.uiLangLocked) === "1";
    }

    function t(key) {
        const lang = getLang();
        return STR[lang][key] ?? STR.zh[key] ?? key;
    }

    function applyToDocument() {
        document.querySelectorAll("[data-i18n]").forEach(el => {
            const key = el.getAttribute("data-i18n");
            const val = t(key);
            if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
                if (el.hasAttribute("data-i18n-placeholder") || el.hasAttribute("placeholder")) {
                    el.placeholder = t(el.getAttribute("data-i18n-placeholder") || key);
                } else {
                    el.textContent = val;
                }
            } else if (el.tagName === "OPTION") {
                el.textContent = val;
            } else {
                el.textContent = val;
            }
        });
        document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
            if (!el.hasAttribute("data-i18n")) {
                el.placeholder = t(el.getAttribute("data-i18n-placeholder"));
            }
        });
        document.querySelectorAll("[data-i18n-title]").forEach(el => {
            el.title = t(el.getAttribute("data-i18n-title"));
        });
        document.querySelectorAll("[data-i18n-aria-label]").forEach(el => {
            el.setAttribute("aria-label", t(el.getAttribute("data-i18n-aria-label")));
        });
        const pageTitleKey = document.body?.getAttribute("data-i18n-page-title");
        if (pageTitleKey) document.title = t(pageTitleKey);
        document.documentElement.lang = getLang() === "en" ? "en" : "zh";
    }

    function syncLangBarButtons() {
        const bar = document.getElementById("csLangBar");
        if (!bar) return;
        const lang = getLang();
        const locked = isLangLocked();
        bar.querySelectorAll("[data-lang]").forEach(btn => {
            const l = btn.getAttribute("data-lang");
            btn.classList.toggle("active", lang === l);
            btn.disabled = locked;
        });
        bar.classList.toggle("lang-locked", locked);
        const tip = bar.querySelector(".lang-locked-tip");
        if (tip) tip.classList.toggle("hidden", !locked);
    }

    function mountLangBar(opts) {
        opts = opts || {};
        let bar = document.getElementById("csLangBar");
        if (!bar) {
            bar = document.createElement("div");
            bar.id = "csLangBar";
            bar.className = "lang-bar";
            bar.innerHTML =
                `<button type="button" class="lang-btn" data-lang="zh" id="csLangZh">华语</button>` +
                `<button type="button" class="lang-btn" data-lang="en" id="csLangEn">English</button>` +
                `<span class="lang-locked-tip hidden" data-i18n="langLocked"></span>`;
            (opts.slot || document.body).appendChild(bar);
            bar.querySelector('[data-lang="zh"]').addEventListener("click", () => {
                if (setLang("zh")) {
                    applyToDocument();
                    if (opts.onChange) opts.onChange("zh");
                }
            });
            bar.querySelector('[data-lang="en"]').addEventListener("click", () => {
                if (setLang("en")) {
                    applyToDocument();
                    if (opts.onChange) opts.onChange("en");
                }
            });
        } else if (opts.slot && bar.parentElement !== opts.slot) {
            opts.slot.appendChild(bar);
        }
        bar.classList.toggle("lang-bar--inline", !!opts.slot);
        if (!opts.slot && bar.parentElement !== document.body) {
            document.body.appendChild(bar);
            bar.classList.remove("lang-bar--inline");
        }
        syncLangBarButtons();
        applyLangBarVisibility();
        applyToDocument();
    }

